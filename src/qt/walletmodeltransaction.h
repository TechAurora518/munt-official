#ifndef GULDEN_QT_WALLETMODELTRANSACTION_H
#define GULDEN_QT_WALLETMODELTRANSACTION_H

#include "walletmodel.h"
#include "wallet/wallet.h"

#include <QObject>

class SendCoinsRecipient;

class CReserveKeyOrScript;
class CWallet;
class CWalletTx;

/** Data model for a walletmodel transaction. */
class WalletModelTransaction
{
public:
    explicit WalletModelTransaction(const QList<SendCoinsRecipient> &recipients);
    ~WalletModelTransaction();

    QList<SendCoinsRecipient> getRecipients();

    CWalletTx *getTransaction();
    unsigned int getTransactionSize();

    void setTransactionFee(const CAmount& newFee);
    CAmount getTransactionFee();

    CAmount getTotalTransactionAmount();

    void newPossibleKeyChange(CAccount* forAccount, CWallet *wallet);
    CReserveKeyOrScript *getPossibleKeyChange();

    void reassignAmounts(int nChangePosRet); // needed for the subtract-fee-from-amount feature

private:
    QList<SendCoinsRecipient> recipients;
    CWalletTx *walletTransaction;
    CReserveKeyOrScript *keyChange;
    CAmount fee;
};

#endif // GULDEN_QT_WALLETMODELTRANSACTION_H
